package base.dto;

public class AreaDto {
	String area_name;		 //이름
	String area_address;	 //주소
	String area_explanation; //설명
	
	
	public String getArea_name() {
		return area_name;
	}
	public void setArea_name(String area_name) {
		this.area_name = area_name;
	}
	public String getArea_address() {
		return area_address;
	}
	public void setArea_address(String area_address) {
		this.area_address = area_address;
	}
	public String getArea_explanation() {
		return area_explanation;
	}
	public void setArea_explanation(String area_explanation) {
		this.area_explanation = area_explanation;
	}

}